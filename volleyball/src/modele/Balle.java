package modele;

/**
 * @author Aurélie Durand
 *
 */
public class Balle {

	private float x;
	private float y;
	
	public Balle(int x, int y) {
		this.x = x;
		this.y = y;
	}

	public float getX() {
		return x;
	}

	public void setX(float x) {
		this.x = x;
	}

	public float getY() {
		return y;
	}

	public void setY(float y) {
		this.y = y;
	}

}
